<?php

namespace App\Http\Controllers\Api;

use App\Http\Controllers\Controller;
use App\Models\User;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;
use Illuminate\Support\Facades\Hash;
use Illuminate\Validation\ValidationException;

class AuthController extends Controller
{
    public function login(Request $request)
    {
        $request->validate([
            'email' => 'required|email',
            'password' => 'required',
            'device_name' => 'required',
        ]);

        $user = User::where('email', $request->email)->first();

        if (!$user || !Hash::check($request->password, $user->password)) {
            throw ValidationException::withMessages([
                'email' => ['The provided credentials are incorrect.'],
            ]);
        }

        // Optional: Check if user actually belongs to this tenant? 
        // In this architecture, Users are central but linked via Roles/TenantUser pivot?
        // Wait, current setup: Users are in the Tenant DB (multi-database)?
        // Re-verifying architecture:
        // Config says 'tenant_model' => \App\Models\Tenant::class.
        // Users are usually in the Tenant Database if we are using "Tenancy for Laravel" with multi-database.
        // Let's assume standard behavior: User model is in Tenant DB.

        return response()->json([
            'token' => $user->createToken($request->device_name)->plainTextToken,
            'user' => $user,
        ]);
    }

    public function logout(Request $request)
    {
        $request->user()->currentAccessToken()->delete();

        return response()->json(['message' => 'Logged out successfully']);
    }
}
